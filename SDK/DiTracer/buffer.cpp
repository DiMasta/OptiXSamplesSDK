#include "buffer.h"

DiBuffer::DiBuffer() :
	name()
{
	buffer = new RTbuffer();
	variable = new RTvariable();
}

//**************************************************************************************************************************
//**************************************************************************************************************************

DiBuffer::~DiBuffer() {
	if (buffer) {
		delete buffer;
		buffer = NULL;
	}

	if (variable) {
		delete variable;
		variable = NULL;
	}
}

//**************************************************************************************************************************
//**************************************************************************************************************************

RTbuffer* DiBuffer::getBuffer() const {
	return buffer;
}

//**************************************************************************************************************************
//**************************************************************************************************************************

RTvariable* DiBuffer::getVariable() const {
	return variable;
}

//**************************************************************************************************************************
//**************************************************************************************************************************

std::string DiBuffer::getName() const {
	return name;
}

//**************************************************************************************************************************
//**************************************************************************************************************************


void DiBuffer::setBuffer(RTbuffer* buffer) {
	this->buffer = buffer;
}

//**************************************************************************************************************************
//**************************************************************************************************************************

void DiBuffer::setVariable(RTvariable* variable) {
	this->variable = variable;
}

//**************************************************************************************************************************
//**************************************************************************************************************************

void DiBuffer::setName(std::string name) {
	this->name = name;
}