#include "geometry.h"

//**************************************************************************************************************************
//**************************************************************************************************************************

DiGeometry::DiGeometry() :
	SceneElement()
{
	geometry = new RTgeometry();
}

//**************************************************************************************************************************
//**************************************************************************************************************************

DiGeometry::~DiGeometry() {
	if (geometry) {
		delete geometry;
		geometry = NULL;
	}
}

//**************************************************************************************************************************
//**************************************************************************************************************************

RTgeometry* DiGeometry::getRTGeometry() const {
	return geometry;
}

//**************************************************************************************************************************
//**************************************************************************************************************************

void DiGeometry::setRTGeoemtry(RTgeometry* geometry) {
	this->geometry = geometry;
}
